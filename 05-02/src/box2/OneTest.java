package box2;
class One{
    int value;
    public One(){
        this(100);
    }
    public One(int value){
       this.value = value;
       Another.methodA(this); // Another 클래스 메소드 호출	
   }
}
class Another{
    static void methodA(One ins){    // 클래스 메소드
          System.out.println("메소드A에서의 값: " + ins.value);
    }
}
public class OneTest {
	public static void main(String[] args) {
		 One t1 = new One();
         System.out.println("기본 값: " + t1.value);
         int value = Integer.parseInt(args[0]);
         One t2 = new One(value);
         System.out.println("사용자가 입력한 값: " + t2.value);
	}
}
